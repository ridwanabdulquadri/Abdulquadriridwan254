public class Calculate {
    public static int addNumber(int num1, int num2, int num3) {
        return (num1 + num2 + num3);
    }

    public static int minusNumbers(int num1, int num2, int num3) {
        return (num1 + num2 - num3);
    }

    public static int multiplyNumbers(int i1, int i2, int i3, int i4, int i5, int i6, int i7, int i8, int i9, int i10, int i11, int i12, int i13, int i14,int i15) {
        return (i1*i2*i3*i4*i5*i6*i7*i8*i9*i10*i11*i12*i13*i14*i15);
    }

    public static int divideThisNumber(int i1, int i2) {
        return (i1 / i2);
    }

    public static double getTheDecimal(int i1, int i2) {
        return (i1 / i2);
    }
}
